    public function send( $phone, $message, $settings ) {
        $service = trim( $settings['service_name'] ?? '' );
        $ak      = trim( $settings['app_key']      ?? '' );
        $as      = trim( $settings['app_secret']   ?? '' );
        $ck      = trim( $settings['consumer_key'] ?? '' );
        $sender  = trim( $settings['sender']       ?? '' );

        if ( $service === '' || $ak === '' || $as === '' || $ck === '' || $sender === '' ) {
            return [
                'success'    => false,
                'message_id' => null,
                'error'      => __( 'OVH SMS: one or more credentials are missing (service, AK, AS, CK, sender).', 'sms-reminder-for-amelia' ),
            ];
        }

        $url = self::API_BASE . '/sms/' . rawurlencode( $service ) . '/jobs';

        $body_data = [
            'charset'         => 'UTF-8',
            'coding'          => '8bit', // 8bit lets us use accents; 7bit is cheaper but ASCII only
            'message'         => $message,
            'receivers'       => [ $phone ],
            'sender'          => $sender,
            'senderForResponse' => false,
            'noStopClause'    => ! empty( $settings['no_stop_clause'] ),
            'priority'        => 'high',
        ];
        $body_json = wp_json_encode( $body_data );

        // OVH signed request
        $timestamp = $this->fetch_server_time();
        if ( $timestamp === null ) {
            return [
                'success'    => false,
                'message_id' => null,
                'error'      => __( 'OVH SMS: unable to fetch API server time.', 'sms-reminder-for-amelia' ),
            ];
        }

        $to_sign   = $as . '+' . $ck . '+POST+' . $url . '+' . $body_json . '+' . $timestamp;
        $signature = '$1$' . sha1( $to_sign );

        $response = wp_remote_post( $url, [
            'timeout'     => 20,
            'httpversion' => '1.1',
            'headers'     => [
                'X-Ovh-Application' => $ak,
                'X-Ovh-Consumer'    => $ck,
                'X-Ovh-Timestamp'   => $timestamp,
                'X-Ovh-Signature'   => $signature,
                'Content-Type'      => 'application/json;charset=utf-8',
            ],
            'body'        => $body_json,
        ] );

        if ( is_wp_error( $response ) ) {
            return [
                'success'    => false,
                'message_id' => null,
                'error'      => __( 'Network error:', 'sms-reminder-for-amelia' ) . ' ' . $response->get_error_message(),
            ];
        }

        $http_code = wp_remote_retrieve_response_code( $response );
        $data      = json_decode( wp_remote_retrieve_body( $response ), true );

        // Success: OVH returns 200 with ids (array) and validReceivers (array) + totalCreditsRemoved.
        if ( $http_code === 200 && ! empty( $data['ids'] ) && is_array( $data['ids'] ) ) {
            return [
                'success'    => true,
                'message_id' => (string) $data['ids'][0],
                'error'      => null,
            ];
        }

        $msg = isset( $data['message'] ) ? $data['message'] : __( 'Unknown error', 'sms-reminder-for-amelia' );
        return [
            'success'    => false,
            'message_id' => null,
            'error'      => sprintf( 'OVH: %s (HTTP %s)', $msg, $http_code ),
        ];
    }

    public function handle_dlr( WP_REST_Request $request ) {
        // OVH can POST JSON or x-www-form-urlencoded depending on configuration.
        $body = $request->get_json_params();
        if ( empty( $body ) ) {
            $body = $request->get_params();
        }

        $message_id = $body['id'] ?? ( $body['messageId'] ?? null );
        $bstatus    = $body['bstatus'] ?? ( $body['status'] ?? null );

        if ( empty( $message_id ) || $bstatus === null ) {
            return null;
        }

        // OVH bstatus codes: -1 pending, 0/1/2 delivered (varies), >= 4 failed.
        $code = (int) $bstatus;
        if ( $code === -1 ) {
            $internal = 'pending';
        } elseif ( $code >= 0 && $code <= 2 ) {
            $internal = 'delivered';
        } else {
            $internal = 'failed';
        }

        return [
            'message_id' => sanitize_text_field( (string) $message_id ),
            'status'     => $internal,
        ];
    }

    /**
     * OVH signed requests require a timestamp within a few seconds of OVH server time.
     * Fetching /auth/time avoids clock drift issues.
     *
     * @return int|null Unix timestamp or null on failure.
     */
    protected function fetch_server_time() {
        $response = wp_remote_get( self::API_BASE . '/auth/time', [ 'timeout' => 10 ] );
        if ( is_wp_error( $response ) ) { return null; }
        $ts = trim( wp_remote_retrieve_body( $response ) );
        if ( ! ctype_digit( $ts ) ) { return null; }
        return (int) $ts;
    }
}
