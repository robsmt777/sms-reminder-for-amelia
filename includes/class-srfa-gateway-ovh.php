/**
 * OVH SMS gateway (https://www.ovhtelecom.fr/sms/).
 *
 * Uses the OVH API v1.0 with signed requests (AK + AS + CK).
 * Endpoint: POST /sms/{serviceName}/jobs
 *
 * Auth is a SHA1 signature of: "AS+CK+METHOD+URL+BODY+TIMESTAMP".
 *
 * DLR: OVH sends a callback with bstatus, statusDetails, datas, etc.
 * We identify the message by the ID OVH returned on send.
 */

if ( ! defined( 'ABSPATH' ) ) { exit; }

class SRFA_Gateway_OVH implements SRFA_Gateway_Interface {

    const API_BASE = 'https://eu.api.ovh.com/1.0';

